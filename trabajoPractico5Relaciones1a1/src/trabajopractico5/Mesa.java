
package trabajopractico5;


public class Mesa {
    private int numero;
    private int capcidad;

    public Mesa(int numero, int capcidad) {
        this.numero = numero;
        this.capcidad = capcidad;
    }

    public int getNumero() {
        return numero;
    }

    public int getCapcidad() {
        return capcidad;
    }
    
    
    
}
